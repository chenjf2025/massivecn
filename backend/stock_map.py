def get_stock_info(symbol: str) -> tuple[str, str]:
    """
    获取股票信息
    
    Args:
        symbol: 股票代码 (如 sh600000, sz000001)
        
    Returns:
        (股票名称, 拼音简称)
    """
    # 统一转为小写
    symbol = symbol.lower().strip()
    return STOCK_MAP.get(symbol, ("", ""))

def search_stocks(keyword: str) -> list[dict]:
    """
    搜索股票
    
    Args:
        keyword: 搜索关键词 (代码、名称、拼音)
        
    Returns:
        匹配的股票列表
    """
    keyword = keyword.lower().strip()
    results = []
    
    for code, (name, pinyin) in STOCK_MAP.items():
        # 匹配代码、名称或拼音
        if (keyword in code.lower() or 
            keyword in name.lower() or 
            keyword in pinyin.lower()):
            results.append({
                "symbol": code,
                "name": name,
                "pinyin": pinyin
            })
    
    return results
